#include <iostream>
using namespace std;

int main() {
    //int
    int myNum = 1000;
    cout << myNum;

    //float
    float myNum = 5.75;
    cout << myNum;

    //double
    double myNum = 19.99;
    cout << myNum;

    //scientific
    float f1 = 35e3;
    double d1 = 12E4;
    cout << f1;
    cout << d1;

    cout << endl;
    return 0;
}